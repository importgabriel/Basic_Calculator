public class Calculator {

    public int add(int num1, int num2){

        return num1 + num2;
    }
    public int subs(int num1, int num2){

        return num1 / num2;
    }
    public int multi(int num1, int num2){

        return num1 * num2;
    }
    public int divide(int num1, int num2){

        return num1 / num2;
    }
    public int modulo(int num1, int num2){

        return num1 % num2;
    }

}
